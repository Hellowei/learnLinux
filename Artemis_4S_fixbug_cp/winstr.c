BOOL CopySmallFile(const char *src, const char *dest)
{
	BOOL ret = FALSE;
	if (CheckFileExist(src))
	{
		UINT32 len;
		char *buf;
		if (NULL != (buf = (char*)LoadSmallFile(src, &len)))
		{
			FILE *fp = fopen(dest, "wb");
			if (fp)
			{
				if (fwrite(buf, len, 1, fp))
				{
					ret = TRUE;
				}
				fclose(fp);
			}
			free(buf);
		}
	}
	return ret;
}

