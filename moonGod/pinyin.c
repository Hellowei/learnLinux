UCHAR *GetPyIndex (UCHAR *str)
{
	UINT32  *ptr;
	UCHAR yunmu[6];
	UCHAR ch[6];
	UCHAR i;
	if (*str < 'a' || *str > 'z')
		return 0;
	if(*str == 'i' || *str == 'u' || *str == 'v')
		return 0;
	
	for(i = 0; i < 6; i++)
	{
		if (*(str+i) >= 'a' && *(str+i) <= 'z')
			ch[i] = *(str+i);
		else
			ch[i] = ' ';
	}

	i = 1;

	/* ¶¨Î»ÉùÄ¸ */
	if(ch[1] == 'h')
	{
		switch(ch[0])
		{
		case 'c':
			ptr = &PyIndex_ch[0][0];
			i  = 2;
			break;
		case 's':
			ptr = &PyIndex_sh[0][0];
			i  = 2;
			break;
		case 'z':
			ptr = &PyIndex_zh[0][0];
			i  = 2;
			break;
		default:
			return 0;
		}
	} 
	else
	{
		ptr = (UINT32*)PyIndexPointer[ch[0] - 'a'];
	}
	/* ¶¨Î»ÉùÄ¸Íê±Ï£¬¿ªÊ¼²éÕÒÔÏÄ¸ */
	ptr++; //PyIndex_**[0][1]
	while(*ptr > 0)
	{
		strcpy(yunmu,(UCHAR*)*ptr);
		if(0 == strncmp(yunmu, &ch[i], 4))
		{
			ptr--;
			return (UCHAR*)*ptr;
		}
		ptr += 2;
	}
	return 0;
}


